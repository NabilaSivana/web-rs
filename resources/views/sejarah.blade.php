            <p>⦁ Pada Tanggal 18 Maret 2013 berdasarkan Keputusan Menteri Kesehatan Republik Indonesia Nomor HK.02.03/I/0501/2013 Rumah Sakit Umum Kabupaten Tangerang ditingkatkan menjadi Rumah Sakit Kelompok B Pendidikan Satelit FKUI.
