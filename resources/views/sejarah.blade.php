            <h2>Hari Jadi</h2>
            <p>Hari jadi Rumah Sakit Umum Kabupaten Tangerang ditetapkan berdasarkan Surat Keputusan Direktur Nomor : 250/131.1/V/1983 yaitu tanggal 5 Mei 1964 sebagai Hari Jadi Rumah Sakit Umum Tangerang yang ditandai dengan digunakannya Rumah Sakit yang berlokasi di Jalan Jenderal Ahmad Yani No.9 Tangerang.</p>

            <h2>Motto</h2>
            <p>Berdasarkan Surat Keputusan Direktur Rumah Sakit Umum Kabupaten Tangerang Tanggal 4 Januari 2014 Nomor : 445/0118/PPE tentang Rencana Strategis Bisnis Motto Rumah Sakit Umum Kabupaten Tangerang adalah <b>“ Kami Ada Untuk Anda “</b></p>

            <h2>Tipe/Kelas Rumah Sakit</h2>
